package lec20_01_java_linkedList;

public class StudyGroup {
	// declaring some variables
	public String fname;
	public int age;
	public String lname;

	public StudyGroup() {
		System.out.println("default constructor");
	}

	public StudyGroup(String fname, int age, String lname) {
		super();
		this.fname = fname;
		this.age = age;
		this.lname = lname;
	}

}
